num1 = int(input('Digite o primeiro numero: '))
num2 = float(input('Digite o Segundo numero: '))

soma = num1 + num2
subtracao = num1 - num2
mutiplicao = num1 * num2
divisao = num1/num2
exponeceacao = num1 ** num2

print(f'Soma = {soma}')
print(f'subtração = {subtracao}')
print(f'mutiplição = {mutiplicao}')
print(f'Divisao = {divisao}')
print(f'Exponencição = {exponeceacao}')
